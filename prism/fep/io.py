#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
File I/O module for PRISM-FEP

This module handles reading PRISM-generated files (ITP and GRO) for FEP processing.
"""

from typing import List, Optional
import numpy as np
from prism.gaussian.utils import normalize_element_symbol
from prism.fep.core.mapping import Atom


def read_ligand_from_prism(itp_file: str, gro_file: str, state: Optional[str] = None) -> List[Atom]:
    """
    Read ligand information from PRISM-generated ITP and GRO files.

    This function reads atomic information from GROMACS ITP and GRO files
    generated by PRISM's force field parameterization.

    Parameters
    ----------
    itp_file : str
        Path to GROMACS ITP file containing atom types, charges, and parameters.
    gro_file : str
        Path to GROMACS GRO file containing atomic coordinates.
    state : str, optional
        Hybrid-topology state selector: ``"a"`` or ``"b"``. When omitted,
        regular ligand ITP files are read normally. When provided for a hybrid
        ITP, dummy atoms are filtered out and state-B atom names are taken from
        ``; name_b (state B): ...`` comments when present.

    Returns
    -------
    List[Atom]
        List of Atom objects containing coordinates, charges, types, etc.

    Notes
    -----
    ITP file format ([ atoms ] section):
        nr type resnr residue atom cgnr charge mass typeB chargeB massB

    GRO file format:
        residue_id residue_name atom_name atom_id x y z

    Coordinate conversion:
        GRO files use nanometers (nm), this function converts to Angstroms (Å)
    """
    normalized_state = state.lower() if state is not None else None
    if normalized_state not in {None, "a", "b"}:
        raise ValueError(f"Unsupported state: {state}. Expected None, 'a', or 'b'.")

    atoms_data = []
    all_atom_names = set()
    pending_name_b = None

    with open(itp_file, "r") as f:
        in_atoms_section = False
        for line in f:
            line_stripped = line.strip()

            if "[ atoms ]" in line.lower():
                in_atoms_section = True
                continue
            if line_stripped.startswith("[") and in_atoms_section:
                in_atoms_section = False
                continue

            if not in_atoms_section:
                continue

            if line_stripped.startswith(";"):
                if normalized_state == "b" and "name_b (state B):" in line_stripped:
                    pending_name_b = line_stripped.split("name_b (state B):", 1)[1].strip()
                continue

            if not line_stripped:
                continue

            # Remove comments (anything after ';')
            comment_idx = line_stripped.find(";")
            if comment_idx != -1:
                line_stripped = line_stripped[:comment_idx].strip()

            parts = line_stripped.split()
            if len(parts) < 8:
                continue

            try:
                atom_id = int(parts[0])
                atom_name = parts[4]
                atom_type_a = parts[1]
                charge_a = float(parts[6])
                atom_type_b = parts[8] if len(parts) >= 11 else None
                charge_b = float(parts[9]) if len(parts) >= 11 else None
            except (ValueError, IndexError):
                pending_name_b = None
                continue

            all_atom_names.add(atom_name)
            atoms_data.append(
                {
                    "id": atom_id,
                    "name": atom_name,
                    "type_a": atom_type_a,
                    "charge_a": charge_a,
                    "type_b": atom_type_b,
                    "charge_b": charge_b,
                    "name_b": pending_name_b,
                }
            )
            pending_name_b = None

    if normalized_state is None:
        selected_atoms = [
            {
                "id": atom["id"],
                "name": atom["name"],
                "type": atom["type_a"],
                "charge": atom["charge_a"],
            }
            for atom in atoms_data
        ]
    else:
        selected_atoms = []
        for atom in atoms_data:
            has_state_b = atom["type_b"] is not None
            atom_name = atom["name"]

            if normalized_state == "a":
                if atom["type_a"].startswith("DUM"):
                    continue
                selected_atoms.append(
                    {
                        "id": atom["id"],
                        "name": atom_name,
                        "type": atom["type_a"],
                        "charge": atom["charge_a"],
                    }
                )
                continue

            if has_state_b:
                if atom["type_b"].startswith("DUM"):
                    continue
                selected_atoms.append(
                    {
                        "id": atom["id"],
                        "name": atom["name_b"] or atom_name,
                        "type": atom["type_b"],
                        "charge": atom["charge_b"],
                    }
                )
                continue

            if atom["type_a"].startswith("DUM"):
                continue

            # FEbuilder may record common A↔B correspondences via `name_b` comments
            # even when a separate `XXXB` atom also exists in the hybrid topology.
            # In that case the comment is authoritative for the B-state identity and
            # must not be discarded by the suffix-based heuristic.
            if atom["name_b"]:
                selected_atoms.append(
                    {
                        "id": atom["id"],
                        "name": atom["name_b"],
                        "type": atom["type_a"],
                        "charge": atom["charge_a"],
                    }
                )
                continue

            has_b_suffix_partner = f"{atom_name}B" in all_atom_names
            if has_b_suffix_partner:
                continue
            selected_atoms.append(
                {
                    "id": atom["id"],
                    "name": atom_name,
                    "type": atom["type_a"],
                    "charge": atom["charge_a"],
                }
            )

    coords_by_id = {}
    coords_by_name = {}

    # Auto-detect file format: PDB or GRO
    with open(gro_file, "r") as f:
        first_line = f.readline().strip()

    is_pdb = first_line.startswith("ATOM") or first_line.startswith("HETATM") or first_line.startswith("REMARK")

    if is_pdb:
        # Read PDB format (fixed column widths)
        with open(gro_file, "r") as f:
            for line in f:
                if line.startswith("ATOM") or line.startswith("HETATM"):
                    atom_name = line[12:16].strip()
                    atom_id = int(line[6:11].strip())
                    x = float(line[30:38].strip())
                    y = float(line[38:46].strip())
                    z = float(line[46:54].strip())

                    try:
                        coord = np.array([x, y, z])
                        coords_by_id[atom_id] = coord
                        coords_by_name[atom_name] = coord
                    except (ValueError, IndexError):
                        continue
    else:
        # Read GRO format (whitespace-separated)
        with open(gro_file, "r") as f:
            lines = f.readlines()

            for line in lines[2:-1]:
                parts = line.split()
                if len(parts) >= 7:
                    atom_name = parts[2]
                    atom_id = int(parts[3])
                    x = float(parts[4]) * 10.0
                    y = float(parts[5]) * 10.0
                    z = float(parts[6]) * 10.0
                elif len(parts) >= 6:
                    atom_name = parts[1]
                    atom_id = int(parts[2])
                    x = float(parts[3]) * 10.0
                    y = float(parts[4]) * 10.0
                    z = float(parts[5]) * 10.0
                else:
                    continue

                try:
                    coord = np.array([x, y, z])
                    coords_by_id[atom_id] = coord
                    coords_by_name[atom_name] = coord
                except (ValueError, IndexError):
                    continue

    atoms = []
    for atom_data in selected_atoms:
        atom_id = atom_data["id"]
        atom_name = atom_data["name"]
        element = _extract_element(atom_name)
        coord = coords_by_id.get(atom_id, coords_by_name.get(atom_name, np.array([0.0, 0.0, 0.0])))

        atoms.append(
            Atom(
                name=atom_name,
                element=element,
                coord=coord,
                charge=atom_data["charge"],
                atom_type=atom_data["type"],
                index=atom_id,
            )
        )

    return atoms


def _extract_element(atom_name: str) -> str:
    """
    Extract element symbol from atom name.

    Parameters
    ----------
    atom_name : str
        Atom name (e.g., 'C1', 'H2', 'N3', 'CA', 'HB1', 'FE', 'Cl')

    Returns
    -------
    str
        Element symbol (e.g., 'C', 'H', 'N', 'Fe', 'Cl')

    Examples
    --------
    >>> _extract_element('C1')
    'C'
    >>> _extract_element('HB1')
    'H'
    >>> _extract_element('CA')
    'C'
    >>> _extract_element('FE')
    'Fe'
    >>> _extract_element('Cl')
    'Cl'
    """
    # Remove digits, keep only letters
    letters = "".join(c for c in atom_name if c.isalpha())

    if not letters:
        return "C"  # Default to carbon

    # For biomolecular atom names (PDB/GROMACS style):
    # - Atom names are typically 1-4 characters
    # - Element is ALWAYS the first letter
    # - Second letter (if present) is typically:
    #   - Uppercase: position code (CA, CB, CG, CD, CE, CZ = carbons)
    #   - Lowercase: part of two-letter element ONLY for rare elements (Fe, Cl, Br, etc.)

    # Common two-letter elements in drug-like molecules that use lowercase
    # These are the ONLY cases where the second letter matters
    organic_two_letter_elements = {"Cl", "Br", "Fe", "Mg", "Ca", "Zn", "Cu", "Mn", "Se", "Si"}

    # Check if this could be a two-letter element
    if len(letters) >= 2:
        # Check case pattern: two-letter elements are like "Cl", "Fe" (upper + lower)
        if letters[0].isupper() and letters[1].islower():
            candidate = letters[0] + letters[1]
            if candidate in organic_two_letter_elements:
                return candidate

    # Default: use first letter only (C, H, N, O, S, P, F, etc.)
    return letters[0].upper()


def read_mol2_atoms(mol2_file: str) -> List[Atom]:
    """
    Read atomic information from MOL2 file (for quick testing).

    This function provides a direct path for testing without requiring
    PRISM parameterization. For production use, prefer read_ligand_from_prism().

    Parameters
    ----------
    mol2_file : str
        Path to MOL2 file

    Returns
    -------
    List[Atom]
        List of Atom objects

    Notes
    -----
    MOL2 file format (@<TRIPOS>ATOM section):
        atom_id atom_name x y z atom_type subst_id subst_name charge

    This function reuses PRISM's mol2 parsing logic from gaussian/utils.py
    """
    atoms = []
    in_atom_section = False

    with open(mol2_file, "r") as f:
        for line in f:
            if line.startswith("@<TRIPOS>ATOM"):
                in_atom_section = True
                continue
            elif line.startswith("@<TRIPOS>") and in_atom_section:
                break

            if in_atom_section and line.strip():
                parts = line.split()
                if len(parts) >= 9:
                    try:
                        atom_id = int(parts[0])
                        name = parts[1]
                        x, y, z = float(parts[2]), float(parts[3]), float(parts[4])
                        atom_type = parts[5]
                        charge = float(parts[8])

                        # Extract element from atom_type
                        if "." in atom_type:
                            element = normalize_element_symbol(atom_type.split(".")[0])
                        else:
                            element = normalize_element_symbol(atom_type)

                        atoms.append(
                            Atom(
                                name=name,
                                element=element,
                                coord=np.array([x, y, z]),
                                charge=charge,
                                atom_type=atom_type,
                                index=atom_id,
                            )
                        )
                    except (ValueError, IndexError) as e:
                        continue

    return atoms


def read_rtf_for_fep(rtf_file: str, pdb_file: str) -> List[Atom]:
    """
    Read ligand from RTF (atom types/charges) and PDB (coordinates) for FEP mapping

    This function is specifically designed for FEP calculations, reading
    RTF files (from CGenFF, MATCH, SwissParam, etc.) and combining with
    PDB coordinates to create Atom objects for mapping.

    Parameters
    ----------
    rtf_file : str
        RTF file path (atom types and charges)
    pdb_file : str
        PDB file path (aligned coordinates)

    Returns
    -------
    List[Atom]
        List of Atom objects with complete information

    Examples
    --------
    >>> atoms = read_rtf_for_fep('ligand.rtf', 'ligand.pdb')
    >>> mapper = DistanceAtomMapper()
    >>> mapping = mapper.map(atoms, other_atoms)
    """
    # Read RTF file for atom types and charges
    rtf_data = {}
    with open(rtf_file, "r") as f:
        for line in f:
            if line.strip().startswith("ATOM"):
                parts = line.split()
                name = parts[1]
                atom_type = parts[2]
                charge = float(parts[3])
                rtf_data[name] = {"type": atom_type, "charge": charge}

    # Read PDB file for coordinates
    atoms = []
    atom_id = 0
    with open(pdb_file, "r") as f:
        for line in f:
            if line.startswith("ATOM") or line.startswith("HETATM"):
                atom_name = line[12:16].strip()

                # Only process atoms that exist in RTF
                if atom_name not in rtf_data:
                    continue

                atom_id += 1
                # Try to get element from PDB column 77-78, but fallback to atom name extraction
                # Many PDB files don't have the element column properly formatted
                element_from_pdb = line[76:78].strip() if len(line) > 77 else ""
                if element_from_pdb and element_from_pdb.isalpha():
                    element = element_from_pdb.upper()
                else:
                    # Extract element from atom name (F1 -> F, C10 -> C, etc.)
                    element = _extract_element(atom_name)

                x = float(line[30:38].strip())
                y = float(line[38:46].strip())
                z = float(line[46:54].strip())

                atoms.append(
                    Atom(
                        name=atom_name,
                        element=element,
                        coord=np.array([x, y, z]),
                        charge=rtf_data[atom_name]["charge"],
                        atom_type=rtf_data[atom_name]["type"],
                        index=atom_id,
                    )
                )

    return atoms


def write_ligand_to_pdb(atoms: List[Atom], output_pdb: str, residue_name: str = "LIG") -> None:
    """
    Write Atom objects to PDB format file.

    This function is the counterpart to read_ligand_from_prism(), converting
    Atom objects back to PDB format for visualization (HTML/PNG) or other uses.

    Parameters
    ----------
    atoms : List[Atom]
        List of Atom objects to write
    output_pdb : str
        Path to output PDB file
    residue_name : str, optional
        Residue name to use in PDB file (default: "LIG")

    Notes
    -----
    PDB ATOM record format (columns):
        1-6:   "ATOM" (record name)
        7-11:  Atom serial number
        13-16: Atom name
        17:    Alternate location (blank)
        18-20: Residue name (right-justified)
        22:    Chain identifier (blank)
        23-26: Residue sequence number
        27:    Insertion code (blank)
        31-38: X coordinate (Å, right-justified, 8.3 format)
        39-46: Y coordinate (Å, right-justified, 8.3 format)
        47-54: Z coordinate (Å, right-justified, 8.3 format)
        55-60: Occupancy (default 1.00)
        61-66: Temperature factor (default 0.00)

    Examples
    --------
    >>> atoms = read_ligand_from_prism("LIG.itp", "LIG.gro")
    >>> write_ligand_to_pdb(atoms, "output.pdb")
    """
    with open(output_pdb, "w") as f:
        f.write("REMARK  Generated from PRISM atoms\n")

        for i, atom in enumerate(atoms, 1):
            x, y, z = atom.coord
            # Match original PDB format: "ATOM      1  N   LIG L   1      ..."
            # Chain identifier is the first letter of residue name (not 'A')
            chain_id = residue_name[0] if residue_name else "A"
            line = f"ATOM  {i:5d}  {atom.name:<4s}{residue_name:>3s} {chain_id}   1    {x:8.3f}{y:8.3f}{z:8.3f}  1.00  0.00      {residue_name} \n"
            f.write(line)

        f.write("TER\n")
        f.write("END\n")
